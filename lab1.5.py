# Using a for loop to print numbers from 1 to 10
print("Using for loop:")
for i in range(1, 11):
    print(i, end=" ")
print()  # Newline for better readability

# Using a while loop to print numbers from 1 to 10
print("Using while loop:")
i = 1
while i <= 10:
    print(i, end=" ")
    i += 1
print()  # Newline for better readability
